package io.javalogist;

public class MathUtils {
	public int add(int a, int b){
		return a+b;
	}
	public int subtract(int a, int b){
		return a-b;
	}
	public int multiply(int a, int b){
		return a*b;
	}	
	public int divide(int a, int b){
		return a/b;
	}
	public double area(double r) {
		return Math.PI* r*r;
	}
}
